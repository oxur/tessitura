//! # Tessitura
//!
//! A musicological library cataloging tool for serious musicians.
//!
//! Tessitura is a CLI-first tool for cataloging, enriching, and searching a
//! personal digital music library with musicological rigor. It maintains a full
//! relational model rooted in the FRBR (Functional Requirements for
//! Bibliographic Records) hierarchy and projects flattened metadata into audio
//! tags when needed.
